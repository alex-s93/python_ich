first_number = int(input("Введите первое число: "))
second_number = int(input("Введите второе число: "))

sum = first_number + second_number
subtract = first_number - second_number
multiplication = first_number * second_number
division = first_number / second_number
remainder = first_number % second_number
number_degree = first_number ** second_number

print("Сумма: ", sum)
print("Разность: ", subtract)
print("Произведение: ", multiplication)
print("Частное: ", division)
print("Остаток от деления: ", remainder)
print("Первое число в степени второго числа: ", number_degree)


